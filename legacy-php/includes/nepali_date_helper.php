    public static function convertToNepali($ad_date)
    {
        $en_year = (int) date('Y', strtotime($ad_date));
        $en_month = (int) date('m', strtotime($ad_date));
        $en_day = (int) date('d', strtotime($ad_date));

        // Base Date: 1944-01-01 -> 2000-09-17 BS
        // Using a closer reference: 2024-04-13 AD -> 2081-01-01 BS
        // Let's use 2024-04-13 = 2081-01-01

        $ref_en_year = 2024;
        $ref_en_month = 4;
        $ref_en_day = 13;

        $ref_bs_year = 2081;
        $ref_bs_month = 1;
        $ref_bs_day = 1;

        $target_en = new DateTime($ad_date);
        $ref_en = new DateTime("$ref_en_year-$ref_en_month-$ref_en_day");

        $diff = $ref_en->diff($target_en);
        $days_diff = (int) $diff->format('%r%a'); // Signed days difference

        // Forward calculation
        if ($days_diff >= 0) {
            $bs_year = $ref_bs_year;
            $bs_month = $ref_bs_month;
            $bs_day = $ref_bs_day + $days_diff;

            while (true) {
                $days_in_month = self::$bs_months[$bs_year][$bs_month - 1];
                if ($bs_day <= $days_in_month) {
                    break;
                }
                $bs_day -= $days_in_month;
                $bs_month++;
                if ($bs_month > 12) {
                    $bs_month = 1;
                    $bs_year++;
                }
            }
        } else {
            // Backward calculation not implemented for now as we deal with current/future dates mostly
            // Fallback to rough matching if backward
            $bs_year = $en_year + 56;
            $bs_month = $en_month + 8;
            if ($bs_month > 12) {
                $bs_month -= 12;
                $bs_year++;
            }
            $bs_day = $en_day + 15; // approximate
            if ($bs_day > 30) {
                $bs_day -= 30;
                $bs_month++;
            }
        }

        return sprintf("%04d-%02d-%02d", $bs_year, $bs_month, $bs_day);
    }
